// Package main is the entry point for the Go program.
// It demonstrates a simple "Hello World" message printed to the console.
// This is often the first program written when learning a new programming language.

package main

// The "fmt" package is used for formatted I/O operations in Go.
// In this code, it's used to print a message to the console.
import "fmt"

func main() {
	// Print the "Hello world to the course!" message to the console.
	// fmt.Println() is a function from the fmt package that prints the text followed by a new line.
	fmt.Println("Hello world to the course!")
}
